let num  = Math.floor(Math.random()*100+1);
let num2  = Math.floor(Math.random()*50+1);

//Sum

const sum = num + num2;
const total = `${num} + ${num2} = ${sum}`;
console.log(total)

//Subtract
const sub = num - num2;
const total1 = `${num} - ${num2} = ${sub}`;
console.log(total1)

///Product
const pro = num * num2;
const total2 = `${num} * ${num2} = ${pro}`;
console.log(total2)

///Divide
const div = num / num2;
const total3 = `${num} / ${num2} = ${div}`;
console.log(total3);